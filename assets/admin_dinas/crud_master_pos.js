    function validasi(){
        var validasiOK = false;
        var kodePos = $("#kodePos").val();
        //var kodeSektor = $("#kodeSektor").val();
        //var namaPos = $("#namaPos").val();
        //var alamat = $("#alamat").val();
        //var status = document.getElementById('status').checked;
        
        var kodeSektor = check_validasi("#kodeSektor", $("#kodeSektor").val());
        var namaPos = check_validasi("#namaPos", $("#namaPos").val());
        var alamat = check_validasi("#alamat", $("#alamat").val());
        if (kodeSektor && namaPos && alamat) {
            validasiOK = true;
        }
        check_duplicate(kodePos, validasiOK);
    }

    function check_duplicate(kodePos, validasiOK){
        //duplikasi = true;
        var hasSpace = /\s/g.test(kodePos);
        if (hasSpace || kodePos.length === 0) {
            check_validasi("#kodePos", '');
        } else {
            var dataString = 'loadId='+ kodePos;
            //console.log('validasiOK = ' + validasiOK);
            $.ajax({
                type: "GET",
                url: base_url + controller+"/check_duplicate_kodePos",
                data: dataString,
                cache: false,
                success: function(result){
                    //console.log(result);
                    //add_attribute(result);
                    if (result == 'no-duplicate') {
                        check_validasi("#kodePos", 'success');
                        if (validasiOK) {
                            save();
                        }
                    }else{
                        check_validasi("#kodePos", '');
                    }
                }
            });
        }
    }

    function check_validasi(target, result_val){
        var invalid;
        if($( target ).hasClass( 'is-valid' )){
            $(target).removeClass( 'is-valid' )
        }
        if($( target ).hasClass( 'is-invalid' )){
            $(target).removeClass( 'is-invalid' )
        }

        //var hasSpace = /\s/g.test(result_val);
        if (result_val.length === 0) {
            invalid = true;
            $(target).addClass( 'is-invalid' );
        } else {
            invalid = false;
            $(target).addClass( 'is-valid' );
        }
        return !invalid;
    }

